# Cast to integers
x = int(27)
y = int(5.8)
z = int("1996")
print("x = {}, y = {}, z ={}".format(x, y, z))
print(type(x), type(y), type(z))
print()

# Cast to float
w = float("78.5")
x = float(27)
y = float(5.8)
z = float("1996")
print("w = {}, x = {}, y = {}, z ={}".format(w, x, y, z))
print(type(w), type(x), type(y), type(z))
print()

# Cast to str
x = str("1996")
y = str(3.0)
z = str(27)
print("x = {}, y = {}, z ={}".format(x, y, z))
print(type(x), type(y), type(z))

