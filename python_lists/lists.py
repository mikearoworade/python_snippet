# Store multiple item in a single variable
names = ["Michael", "Ayodele", "Aroworade"]
print(names)

# lists items are ordered, changeable and allow duplicate values.
names_dup = ["Michael", "Ayodele", "Aroworade", "Michael"]
print(names_dup)

# List length
print(len(names))

# list can contain different data types
bio = ["Michael", "Aroworade", 27, "Male", 5.8, "Software Engineer"]
print(bio)
print(type(bio))
print()

# list constructor
bio1 = list(("Michael", "Aroworade", "Ayodele"))
print(bio1)
print(type(bio1))
