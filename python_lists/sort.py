# Sort Alphabetically
names = ["Michael", "Ayodele", "Oluwaseun", "Aroworade"]
print(names)
names.sort()
print(names)
print()

# Sort Numerically
numbers = [27, 3, 26, 1996, 78]
print(numbers)
numbers.sort()
print(numbers)
print()

# Sort Descending
names = ["Michael", "Ayodele", "Oluwaseun", "Aroworade"]
print(names)
names.sort(reverse=True)
print(names)
print()

numbers = [27, 3, 26, 1996, 78]
print(numbers)
numbers.sort(reverse=True)
print(numbers)
print()

# reverse Order of list
names = ["Michael", "Ayodele", "Oluwaseun", "Aroworade"]
print(names)
names.reverse()
print(names)
