# Built in method for string
name = "Michael Aroworade"
print(name)

# Upper case
print(name.upper())

# Lower case
print(name.lower())

name = "        Michael Aroworade  "
# Remove whitespaces at beginning and end.
print(name.strip())

name = "Michael Aroworade"
# Replace string
print(name.replace("ade", "crown"))

# Split String
print(name.split(" "))
