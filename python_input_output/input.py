# input() function takes input from users and convert it into string
year = input("Enter year: ")
print(year)
print(type(year), end="\n\n")

# Take string as input
name = input("What is your name: ")
print(name)
print(type(name), end="\n\n")

# Desired datatype using casting
month = int(input("Enter birth month: "))
print(month)
print(type(month), end="\n\n")

height = float(input("Enter your height: "))
print(height)
print(type(height), end="\n")
