# Looping using for
names = ("Aroworade", "Michael", "Ayodele")
print(names)
for name in names:
    print(name)
print()

# Loop using len and range
for i in range(len(names)):
    print(names[i])
print()

# Loop using while loop
i = 0
while i <= len(names):
    print(names[i])
    i = i + 1
print()

